    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String name = inputName.getText();
        String author = inputAuthor.getText();
        String stock = inputStock.getText();

        if (name.isEmpty() || author.isEmpty() || stock.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill All TextField");
        } else {
            BookManager.addBook(name, author, stock);
            inputName.setText("");
            inputAuthor.setText("");
            inputStock.setText("");

            JOptionPane.showMessageDialog(this, "Successfully Inserted");
        }


    }//GEN-LAST:event_btnSaveActionPerformed

